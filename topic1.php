        <h1>The History of IoT</h1>
        <p>
          IoT has greatly advanced since its early applications, especially
          within the past twenty years. IoT is now all around us and changing
          all of our lives. Below are some key moments in the history of IoT:
        </p>
      </section>

      <section>
        <h2>Early Applications</h2>
        <aside>
          <p>
            These are not the only early applications of IoT, and many more
            examples were prevelant before the 2000's.
          </p>
        </aside>
        <ol>
          <li>
            <h3>Coke Vending Machine</h3>
            <br />
            The first real application of IoT was a Coke vending machine in the
            1980's made by some university students. The machine used integrated
            micro-switches with the machine connected to a network so that the
            machine could report drink temperature and whether it needed to be
            refilled.
          </li>
          <li>
            <h3>Coffee Pot</h3>
            <br />
            In 1991, Scientists at The University of Cambridge came up with the
            idea of the first webcam prototype, which connected a camera to
            their local computer in the lab. The camera would take pictures of a
            coffee pot in the lab, and these images would be sent to the lab's
            computer, which could then be checked by users to see if the coffee
            pot had any coffee left.
          </li>
        </ol>
      </section>

      <section>
        <h2>The Creation of the Term</h2>
        <p>
          Although examples that utilized the concepts of IoT had been made in
          the past, the actual term 'Internet of Things' wasn't created until
          1999 by Kevin Ashton. While he was working at Procter and Gamble,
          Kevin suggested to put RFID chips on products in order to track them
          through the supply chain. In order to better sell his idea to
          executives, he used the word 'Internet' in his pitch, which was a new
          and exciting word at the time. This name stuck and is now what we use
          to describe the concept today.
        </p>
      </section>

      <section>
        <h2>The Rise of IoT</h2>
        <ol>
          <li>
            <h3>Initial Boom of IoT</h3>
            <br />
            In the Early 2000's, the idea of IoT started getting some serious
            traction in the media. Articles written by outlets such as The
            Guardian, and Forbes raised interest in the concept, leading to the
            1st International Conference on IoT in Switzerland in 2008, Which
            involved discussion of the possibilities of RFID, sensor modules,
            and short wireless communications being used in IoT applications.
          </li>

          <li>
            <h3>Later Boom to Current Day</h3>
            <br />
            This boom in IoT has only continued in the past decade. According to
            a report by cisco, the number of devices connected to a network went
            from roughly 500 Million in 2003 to 12.5 Billion in 2010, and then
            has doubled every 5 years since then, currently sitting at roughly
            50 Billion worldwide.
          </li>
        </ol>

